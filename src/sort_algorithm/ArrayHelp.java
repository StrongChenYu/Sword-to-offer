package sort_algorithm;

public class ArrayHelp {
    public static void printArr(int[] arr) {
        for (int num : arr) {
            System.out.print(num + " ");
        }
        System.out.println();
    }

    public static void printLR(int l, int r) {
        System.out.println("l = " + l + " r = " + r);
    }
}
